package com.rutuja;

// Primitive Data Types in Java: Primitive means any data type which we cannot break even further

public class primitives {
    public static void main(String[] args) {
        int rollNo = 53;      // ---> 4 bytes

        // string is not primitive
        String name = "Rutuja";    // we can break into individual characters

        char letter = 'r';         // character in single quotes
        float marks = 93.67f;     // ---> 4 bytes
        double largeDecimalNumbers = 4348839.8383;     // ---> 8 bytes
        long largeInteger = 38477799472L;               // ---> 8 bytes
        boolean check = true;

        Integer rno = 48;         // wrapper class(provides additional functionalities)   --> used to convert primitive dt to objects.


        // Comments in Java : ignore by the compiler  (ctrl+/)

    }
}
